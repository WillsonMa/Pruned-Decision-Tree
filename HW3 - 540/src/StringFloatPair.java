public class StringFloatPair{
	private String myString;
	private float myFloat;
	
	StringFloatPair(String myString, float myFloat){
		this.myString = myString;
		this.myFloat = myFloat;
	}
	
	String getString(){
		return this.myString;
	}
	
	float getFloat(){
		return this.myFloat;
	}

}